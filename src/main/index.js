import 'regenerator-runtime/runtime';
import { app } from 'electron'; // eslint-disable-line
import path from 'path';
import Window from './modules/Window/Window';
import ModuleManager from './modules/Manager';
import ElectronUIModule from './modules/ElectronUI';
import AppModule from './modules/App';

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\') // eslint-disable-line
}

let mainWindow;
function createMainWindow() {
  mainWindow = new Window('main', {
    width: 1000,
    height: 563,
    resizable: false,
    icon: path.join(__static, 'icons/icon16x.png'),
  }).create();
}

app.on('ready', createMainWindow);

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  if (mainWindow === null) {
    createMainWindow();
  }
});

ModuleManager.registerModule(AppModule, ElectronUIModule);

